CREATE PROCEDURE schedule_interviews()
BEGIN
    DECLARE start_datetime DATETIME DEFAULT '2025-06-23 09:00:00';
    DECLARE current_id INT DEFAULT 3;

    WHILE current_id <= 34 DO
            -- 점심 시간 건너뛰기
            IF HOUR(start_datetime) = 12 THEN
                SET start_datetime = STR_TO_DATE(CONCAT(DATE(start_datetime), ' 13:00:00'), '%Y-%m-%d %H:%i:%s');
            END IF;

            -- 업무 시간 종료 시 다음날 09:00으로
            IF HOUR(start_datetime) >= 18 THEN
                SET start_datetime = STR_TO_DATE(CONCAT(DATE(DATE_ADD(start_datetime, INTERVAL 1 DAY)), ' 09:00:00'), '%Y-%m-%d %H:%i:%s');
            END IF;

            -- 주말 건너뛰기
            IF DAYOFWEEK(start_datetime) = 7 THEN
                SET start_datetime = DATE_ADD(start_datetime, INTERVAL 2 DAY);
                SET start_datetime = STR_TO_DATE(CONCAT(DATE(start_datetime), ' 09:00:00'), '%Y-%m-%d %H:%i:%s');
            ELSEIF DAYOFWEEK(start_datetime) = 1 THEN
                SET start_datetime = DATE_ADD(start_datetime, INTERVAL 1 DAY);
                SET start_datetime = STR_TO_DATE(CONCAT(DATE(start_datetime), ' 09:00:00'), '%Y-%m-%d %H:%i:%s');
            END IF;

            -- 인터뷰 시간 업데이트
            UPDATE interview
            SET
                scheduled_at = start_datetime,
                scheduled_end_at = DATE_ADD(start_datetime, INTERVAL 30 MINUTE)
            WHERE interview_id = current_id;

            -- 다음 인터뷰로 이동
            SET current_id = current_id + 1;
            SET start_datetime = DATE_ADD(start_datetime, INTERVAL 30 MINUTE);
        END WHILE;
END$$

